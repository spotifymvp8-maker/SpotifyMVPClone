"""
Подключение к PostgreSQL и управление сессиями SQLAlchemy.

- engine: пул соединений к БД
- SessionLocal: фабрика сессий (каждый запрос = новая сессия)
- Base: базовый класс для ORM-моделей (app.models.*)
"""

from sqlalchemy import create_engine
from sqlalchemy.orm import declarative_base, sessionmaker

from app.config import DATABASE_URL

# ──────────────────────────────────────────────
# Движок (Engine)
# ──────────────────────────────────────────────
# Engine — главный объект SQLAlchemy, управляет пулом соединений к PostgreSQL.
# Пул соединений — набор заранее открытых соединений, которые переиспользуются.
# Открыть новое соединение к БД дорого (~10ms), поэтому их держат в пуле.
engine = create_engine(
    DATABASE_URL,
    pool_pre_ping=True,  # Перед выдачей соединения из пула делает SELECT 1.
                         # Если соединение "умерло" (PostgreSQL закрыл при простое) —
                         # автоматически переподключается. Предотвращает ошибки типа
                         # "connection closed" после долгого простоя.
    echo=False,          # True — логировать все SQL-запросы в консоль (удобно при отладке).
                         # В production = False, чтобы не засорять логи.
)

# ──────────────────────────────────────────────
# Фабрика сессий
# ──────────────────────────────────────────────
# SessionLocal — это не сессия, а фабрика (класс) для создания сессий.
# Каждый HTTP-запрос создаёт свою сессию через SessionLocal().
#
# autocommit=False — изменения не отправляются в БД автоматически.
#   Нужно явно вызвать db.commit(). Это позволяет объединять несколько
#   операций в одну транзакцию — либо все выполнятся, либо ни одна.
#
# autoflush=False — SQLAlchemy не отправляет SQL автоматически перед каждым SELECT.
#   Иногда нужно явно контролировать момент отправки (например, db.flush() в seed.py
#   для получения auto-generated ID до финального commit).
SessionLocal = sessionmaker(autocommit=False, autoflush=False, bind=engine)

# Base — базовый класс для всех ORM-моделей.
# Все модели в app/models/ наследуются от Base.
# SQLAlchemy использует Base.metadata для хранения информации о всех таблицах.
Base = declarative_base()


def get_db():
    """
    FastAPI dependency: создаёт сессию БД на время обработки одного HTTP-запроса.

    Использование в эндпоинтах:
        db: Session = Depends(get_db)

    Как это работает:
        1. FastAPI вызывает get_db() перед обработчиком запроса
        2. Создаётся сессия db = SessionLocal()
        3. yield db — сессия передаётся в функцию-обработчик
        4. После отправки ответа (даже при исключении) — db.close()
           освобождает соединение обратно в пул

    yield вместо return превращает функцию в генератор.
    FastAPI понимает это как контекстный менеджер (аналог with-блока).
    Блок finally гарантирует закрытие сессии даже при ошибках.
    """
    db = SessionLocal()
    try:
        yield db        # <-- здесь выполняется тело обработчика запроса
    finally:
        db.close()      # выполняется ВСЕГДА: и при успехе, и при исключении


def init_db():
    """
    Альтернатива миграциям: создаёт все таблицы по ORM-моделям.
    В проекте не используется напрямую — вместо этого применяется Alembic.
    Оставлен для удобства быстрого сброса схемы при разработке.
    """
    import app.models  # noqa: F401 — импорт нужен, чтобы модели зарегистрировались в Base.metadata

    Base.metadata.create_all(bind=engine)
